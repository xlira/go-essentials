package main

import "fmt"

func main() {
	fmt.Println("Hello, Go Essentials!")                //String
	fmt.Printf("Hello, I'm %v and I'm an integer\n", 2) //String com formatação de valor, semelhante a C
	fmt.Println(2)                                      //int
	fmt.Println(true)                                   //boolean
	fmt.Println(5 + 2)                                  //expressões
}
